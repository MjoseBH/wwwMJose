    <h2>Bienvenido al proyecto "Apadrina un animal en peligro de extinción"</h2>
    <p>
        La biodiversidad de nuestro planeta está en riesgo. Muchas especies como el 
        <strong>lince ibérico</strong>, la <strong>tortuga marina</strong> o el 
        <strong>gorila de montaña</strong> se encuentran en peligro crítico de extinción.
    </p>
    <p>
        Con este proyecto queremos concienciar a la sociedad y ofrecerte la oportunidad 
        de apadrinar un animal, contribuyendo directamente a su protección y conservación.
    </p>
